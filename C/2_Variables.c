#include <stdio.h>

int main() {
    int x = 70;
    int y = 80;
    
    printf("x = %d입니다.\n", x);
    printf("y = %d입니다.\n", y);
    
    printf("x+y = %d입니다.\n", x+y);
    printf("x-y = %d입니다.\n", x-y);
    printf("x/y = %d입니다.\n", x/y);
    printf("x*y = %d입니다.\n", x*y);
    return 0;
}
